PUTCHAR MACRO VAR
	MOV DL, VAR
	MOV AH, 02H
	INT 21H 
ENDM

COUT MACRO VARAT
	MOV AL, VARAT
	AAM
	ADD AX, 3030H
	MOV BX, AX
	MOV DL, BH
	MOV AH, 2
	INT 21H
	MOV DL, BL
	MOV AH, 02
	INT 21H
ENDM 		

.MODEL SMALL
.DATA
	MSG DB "Current time is => $", 10, 13
.CODE
	MOV AX, @DATA
	MOV DS, AX

	LEA DX, MSG
	MOV AH, 09H
	INT 21H
	
	;RETRIEVE CURRENT TIME
	MOV AH, 2CH
	INT 21H

	;HOUR
	COUT CH
	PUTCHAR ':'

	;MINUTES
	COUT CL
	PUTCHAR ':'

	;SECONDS
	COUT DH

	MOV AH, 4CH
	INT 21H
	END